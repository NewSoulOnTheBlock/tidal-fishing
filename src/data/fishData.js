export const TIME_SEGMENTS = ["dawn", "day", "dusk", "night"];

export function getTimeSegment(hours) {
  const h = ((hours % 24) + 24) % 24;
  if (h >= 5 && h < 8) return "dawn";
  if (h >= 8 && h < 17) return "day";
  if (h >= 17 && h < 20) return "dusk";
  return "night";
}

